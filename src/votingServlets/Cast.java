package votingServlets;

public class Cast {
	
	private int registeredVoters;
	private int castedVotes;
	
	
	public int getRegisteredVoters() {
		return registeredVoters;
	}
	public void setRegisteredVoters(int registeredVoters) {
		this.registeredVoters = registeredVoters;
	}
	public int getCastedVotes() {
		return castedVotes;
	}
	public void setCastedVotes(int castedVotes) {
		this.castedVotes = castedVotes;
	}
	
	

}
